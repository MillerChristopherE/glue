package org.glue.unit.exceptions;

@Typed
public class UnitParsingException extends UnitSubmissionException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2788454487301006621L;

	public UnitParsingException() {
		super();
	}

	public UnitParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UnitParsingException(String arg0) {
		super(arg0);
	}

	public UnitParsingException(Throwable arg0) {
		super(arg0);
	}


	
	
}
